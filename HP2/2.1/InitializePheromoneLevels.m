function pheromoneLevel = InitializePheromoneLevels(numberOfCities, tau0)
    % InitializePheromoneLevels - generates a matrix with pheromone levels 
    %
    % By: Nicholas Granlund
    % Date: Oct 2023
    
    % Create pheromoneLevel matrix
    pheromoneLevel = tau0.*ones(numberOfCities);

    % return
    
end

